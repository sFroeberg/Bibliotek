    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        if(this.getCardLayoutMain().getEmpLoggedIn() == null){
            UI.showErrorDialog("Not logged in!");
            System.exit(1);
        }
        
        //Reset all fields (except dob)
        Component[] components = this.getComponents();
        for(Component currentComp : components){
            if(currentComp instanceof JTextField){
                JTextField field;
                field = (JTextField) currentComp;
                field.setText("");
            }     
        }
        
        //Setup all Actors in comboBox
        List<Actor> actors = this.getCardLayoutMain().getEntityManager().createNamedQuery("Actor.findAll")
                .getResultList();
        actorListCombo.removeAllItems();
        for(Actor current : actors){
            actorListCombo.addItem(current);
        }
        
    }//GEN-LAST:event_formComponentShown

