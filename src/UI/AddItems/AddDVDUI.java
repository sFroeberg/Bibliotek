    private void saveDVDBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveDVDBtnActionPerformed
        Item newItem = new Item();
        newItem.setBarcode(barcodeField.getText());
        newItem.setTitle(titleField.getText());
        newItem.setReleaseYear(Year.of(Integer.parseInt(yearField.getText())).toString());
        newItem.setLocation(locationField.getText());
        newItem.setDescription(descField.getText());
        
        Dvd newDvd = new Dvd();
        newDvd.setItembarcode(barcodeField.getText());
        newDvd.setGenre(genreField.getText());
        newDvd.setDirector(directorField.getText());
        newDvd.setAgeLimit(Integer.parseInt((String) ageLimitCombo.getSelectedItem()));
        newDvd.setProdCountry(prodCountryField.getText());
        
        
        //Set all actors
        ArrayList<Actor> actors = new ArrayList<>();
        int i = 0;
        while(i < modelActor.getSize()){
            actors.add(modelActor.getElementAt(i));
            i++;
        }
        newDvd.setActorCollection(actors);

        try{
                //Save to database
            
                //Item
                this.getCardLayoutMain().getEntityManager().getTransaction().begin();
                this.getCardLayoutMain().getEntityManager().persist(newItem);
                this.getCardLayoutMain().getEntityManager().getTransaction().commit();
                
                //DVD
                this.getCardLayoutMain().getEntityManager().getTransaction().begin();
                this.getCardLayoutMain().getEntityManager().persist(newDvd);
                this.getCardLayoutMain().getEntityManager().getTransaction().commit();
                UI.showInfoDialog("DVD saved");
                this.switchToCard(EmployeeStartUI.class);
            }catch (RollbackException | IllegalStateException e){
                UI.showErrorDialog("Could not save DVD to database");
            }
    }//GEN-LAST:event_saveDVDBtnActionPerformed

    private void actorsListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_actorsListMouseClicked
        JList list = (JList)evt.getSource();
        if (evt.getClickCount() == 2) {
            int index = list.locationToIndex(evt.getPoint());
            modelActor.removeElementAt(index);
        }
    }//GEN-LAST:event_actorsListMouseClicked


